    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        DatabaseConnect db = new DatabaseConnect();
        
        if(jComboBox1.getSelectedIndex() > 8)
        {
            jComboBox3.setEnabled(true);
        }
        
        else jComboBox3.setEnabled(false);
        
        if(jComboBox1.getSelectedIndex() != 0) {
        
            ResultSet rslt = db.loadSubject(jComboBox2.getSelectedItem().toString(), Integer.parseInt(jComboBox1.getSelectedItem().toString()));

            try {
                if(rslt.next() != false) jTextField3.setText(rslt.getObject(1).toString());

                else {
                    JOptionPane.showMessageDialog(this, "No Match Found", "Failed", JOptionPane.ERROR_MESSAGE);
                    jTextField3.setText("");

                }
            }

            catch(Exception ex)
                    {
                        JOptionPane.showMessageDialog(this, "No Match Found", "Failed", JOptionPane.ERROR_MESSAGE);
                    }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

