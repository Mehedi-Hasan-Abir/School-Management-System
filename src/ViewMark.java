    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i, roll;
        String a;
        ResultSet rslt = null;
        DatabaseConnect db = new DatabaseConnect();
        try
        {
        
            if(jComboBox5.getSelectedIndex() == 1)
            {
                roll = 1;
                for(i = 0; i < 5; i++, roll++)
                {
                    jTable1.getModel().setValueAt(roll, i, 0);
                    if( jComboBox1.getSelectedIndex() > 0 )
                    {
                        
                        a = jComboBox1.getSelectedItem().toString() + "_" + String.valueOf(roll);
                        if(jComboBox3.isEnabled() == true)
                            a += jComboBox3.getSelectedItem().toString();
                        
                        rslt = db.loadSubjectMark(jComboBox2.getSelectedItem().toString(), jComboBox4.getSelectedItem().toString(),jComboBox1.getSelectedItem().toString() , a);
                    }
                    
                    if(rslt.next() != false)
                    {
                        jTable1.getModel().setValueAt(rslt.getObject(1), i, 1);
                    }
                }
            }

            else if(jComboBox5.getSelectedIndex() == 2)
            {
                roll = 6;
                for(i = 0; i < 5; i++, roll++)
                {
                    jTable1.getModel().setValueAt(roll, i, 0);
                    if( jComboBox1.getSelectedIndex() > 0 ){
                        
                        a = jComboBox1.getSelectedItem().toString() + "_" + String.valueOf(roll);
                        if(jComboBox3.isEnabled() == true)
                            a += jComboBox3.getSelectedItem().toString();
                        
                        rslt = db.loadSubjectMark(jComboBox2.getSelectedItem().toString(), jComboBox4.getSelectedItem().toString(),jComboBox1.getSelectedItem().toString() , a);
                    }
                    if(rslt.next() != false)
                    {
                        jTable1.getModel().setValueAt(rslt.getObject(1), i, 1);
                    }
                }
            }
            
            jComboBox1.setSelectedIndex(0);
            
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

